/* ============================================================ */
/* 関数名 : FnU2_AppCtrl_Accel_ctrlPwmFreq                      */
/*          PWM出力周波数制御                                   */
/* 引数   : [us]経過時間                                        */
/* 戻り値 : [us]PWM出力周波数                                   */
/* 概要   : 出力経過時間に応じてPWM出力周波数を制御する         */
/* 制約   : なし                                                */
/* ============================================================ */
U2 FnU2_AppCtrl_Accel_ctrlPwmFreq(U4 tu4ElapsedTime, U4 tu4SizePwmFreqMap, const ST_AppCtrl_Accel_PwmFreqMap tstaPwmFreqMap[])
{
  U4 tu4Idx;
  U2 tu2PwmFreq;

  for (tu4Idx = (U4)0; tu4Idx < tu4SizePwmFreqMap; tu4Idx++) {
    if (tu4ElapsedTime < tstaPwmFreqMap[tu4Idx].u4ElapsedTime) {
      break;
    }
  }
  tu2PwmFreq = tstaPwmFreqMap[tu4Idx].u2PwmFreq;

  return (tu2PwmFreq);
}
#endif

